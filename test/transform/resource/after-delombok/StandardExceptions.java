class EmptyStandardException extends Exception {
	@java.lang.SuppressWarnings("all")
	public EmptyStandardException() {
	}

	@java.lang.SuppressWarnings("all")
	public EmptyStandardException(final String message) {
		super(message);
	}

	@java.lang.SuppressWarnings("all")
	public EmptyStandardException(final Throwable cause) {
		super(cause);
	}

	@java.lang.SuppressWarnings("all")
	public EmptyStandardException(final String message, final Throwable cause) {
		super(message, cause);
	}
}